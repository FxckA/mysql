		  <div class="text2">
		    <div class="txt_w1">
              <p>
                实验室定位于基础研究和应用研究相结合，主要针对广西及西南地区拥有的珍稀濒危动植物资源以及经济社会发展对资源开发利用和生态环境保护的需求开展研究。以珍稀濒危动植物生态与保护、特色动植物资源开发与利用、生物修复与生态重建、环境污染分析与治理技术为主要研究方向。</p>
              <p>实验室拥有一支以中青年教授为主、结构合理的研究队伍。现有固定人员60名，其中教授24人，副教授18人，高级工程师6人，博士30多人。研究队伍中，广西"新世纪十百千人才工程"第二层次人选3名，广西高校百名中青年学科带头人5名。由实验室主要成员组成的"广西野生动植物生态学研究和生态环境保护研究团队"是广西高校人才小高地创新团队。实验室涵盖生态学、野生动植物保护与利用、分子生物学与生物化学、环境科学、应用化学、化学工艺学等10个硕士专业。有硕士导师38人，每年可以培养硕士研究生30人以上。目前已招收硕士生合计300余人，授予硕士学位合计100余人，目前在读研究生200多人。
